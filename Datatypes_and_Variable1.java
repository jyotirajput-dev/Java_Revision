package Practice1;

// Program: Datatypes and Variables
// Description: Demonstrates variables, data types, and naming conventions in Java.

public class Datatypes_and_Variable1 {

    public static void main(String[] args) {

        int age = 20;

        System.out.println("Age: " + age);

        // Variable: A memory location used to store data
        // Data Type: Defines what type of data can be stored

        /*
        Rules for Variables:
        1. Cannot start with a digit
        2. Case-sensitive
        3. Cannot use keywords (e.g., int, void)
        4. No spaces allowed
        5. Can include letters, digits, _ and $
        */

        /*
        Naming Conventions:

        1. PascalCase (for Classes)
           Example: MyClass, MyEmployee, MyLearningJourney

        2. camelCase (for Variables & Methods)
           Example: myScanner, myEmployee, myLearningJourney
        */
    }
}
